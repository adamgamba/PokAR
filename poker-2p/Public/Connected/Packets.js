// makeSyncValue
// -------------
// Set a value, that when set will
// be shared with the other players.
// New joiners will also recieve this
// value.
// Path must be unique and in the format
// of /foo/ or /foo/bar/ or /foo/bar/cat/ etc.

function makeSyncValue (path, clientId) {
    if (!checkPathOk(path)) {
        throw new Error ('makeSync path must be in the format "/foo/"');
    }
    var events = new utils.Events();
    var isDisposed = false;
    var currentValue;

    var share = function (value) {
        currentValue = value;
        packets.send(path + clientId + '/', value);
    }

    function handleShare (body, params, userId) {
        events.trigger('udpated', body, params.cid, userId);
    }
    // e.g. /foo/ross/
    on(path + ':cid/', handleShare);

    function handleRequest (body, params, userId) {
        packets.send(path + clientId + '/', value);
    } 
    // e.g. /foo/request/ross/
    on(path + 'request/:cid/', handleRequest);

    send(path + 'request/' + clientId + '/');

    return {
        on: function (fn) {
            events.on('updated', fn);
        },
        off: function (fn) {
            events.off('updated', fn);
        },
        set: function (value) {
            share(value);
        },
        dispose: function () {
            isDisposed = true;
            off(path + 'request/:cid/', handleRequest);
            off(path + ':cid/', handleShare);
        },
        getIsDisposed: function () {
            return isDisposed;
        }
    }   
}

// makeSync
// --------
// Set a value, that when set will
// be shared with the other players
// only when it is set
// New joiners will not get this value
// Path must be unique and in the format
// of /foo/ or /foo/bar/ or /foo/bar/cat/ etc.

function makeBroadcastValue (path, clientId) {
    if (!checkPathOk(path)) {
        throw new Error ('makeSync path must be in the format "/foo/"');
    }
    var events = new utils.Events();
    var isDisposed = false;
    var currentValue;

    var share = function (value) {
        currentValue = value;
        send(path + clientId + '/', value);
    }

    function handleShare (body, params, userId) {
        events.trigger('updated', body, params.cid, userId);
    }
    on(path + ':cid/', handleShare);

    return {
        on: function (fn) {
            events.on('updated', fn);
        },
        off: function (fn) {
            events.off('updated', fn);
        },
        set: function (value) {
            share(value);
        },
        dispose: function () {
            isDisposed = true;
            off(path + ':cid/', handleShare);
        },
        getIsDisposed: function () {
            return isDisposed;
        }
    }
}

function checkPathOk (path) {
    return path.length > 2 && path[0] === '/' && path[path.length-1] === '/';
}

