var utils = global.utils;

// Packets
// -------
// Path based routing for messages
// Paths must be in the format /foo/, /foo/bar/, or /foo/bar/fish/ etc
// Path can be subscribe with params. For example /foo/:x/ will match /foo/bar/ and with invoke with the param object {x:'bar'}
// Packets will always start with a '/' character. If you project has another system listening to multiplayerSession.onMessage
// ensure it ignores messages starting with '/'

// API
// ---
script.api.send = send;             // Send a packet (path:string, body:string):void
script.api.sendObject = sendObject; // Send a packet (path:string, obj:Object):void
script.api.on = on;                 // Subscribe to a path (path:string, (body:string, param:Object, userId:string):void ):void
script.api.once = once;             // Subscribe for only on match (path:string, (body:string, param:Object, userId:string):void ):void
script.api.off = off;               // Subscribe to a path (path:string, (body:string, param:Object, userId:string):void ):void

// Message sending and receiving 
// -----------------------------
var listeners = [];

function send(path, body) {
    if (typeof body === 'undefined') {
        body = '';
    } 
    global.connectedController.api.sendStringMessage(path + '\n' + body);
}

function sendObject(path, object) {
    global.connectedController.api.sendStringMessage(path + '\n' + JSON.stringify(object));
}

function on(path, fn) {
    listeners.push({
        path: path,
        match: match(path),
        handler: fn,
        isOnce: false
    });
}

function once(path, fn) {
    listeners.push({
        path: path,
        match: match(path),
        handler: fn,
        isOnce: true
    });
}

function off(path, fn) {
    listeners = listeners.reduce(function (acc, listener) {
        if (listener.pattern !== path && listener.fn !== fn) {
            acc.push(listener);
        }
        return acc;
    }, []);
}

// Handle incoming messages
utils.delay0(function () {
    global.connectedController.api.events.on('message received', function (userId, message) {
        if (message[0] !== '/') {
            return;
        }
        var messagePath = utils.getFirstLine(message);
        var messageBody = utils.getAfterFirstLine(message);

        listeners.forEach(function (listener) {
            var match = listener.match(messagePath);
            if (match) {
                if (listener.isOnce) {
                    off(listener.path, listener.handler);
                }
                listener.handler(messageBody, match.params, userId);
            }
        });
    });
});


